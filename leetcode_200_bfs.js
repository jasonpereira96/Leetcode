/**
 * @param {character[][]} grid
 * @return {number}
 */
var numIslands = function (grid) {
    var visited = [];
    var islands = 0;
    for (var row = 0; row < grid.length; row++) {
        visited.push(new Array(grid[0].length).fill(false));
    }

    for (var row = 0; row < grid.length; row++) {
        for (var col = 0; col < grid[0].length; col++) {
            if (!visited[row][col] && grid[row][col] === '1') {
                bfs(row, col);
            }
        }
    }

    function bfs(row, col) {
        var queue = [{
            row, col
        }];
        while (!isEmpty(queue)) {
            var { row, col } = queue.shift();
            if (visited[row][col]) {
                continue;
            }
            visited[row][col] = true;
            if (grid[row + 1] !== undefined && grid[row + 1][col] === '1' && !visited[row + 1][col]) {
                queue.push({
                    row: row + 1, col: col
                });
            }
            if (grid[row - 1] !== undefined && grid[row - 1][col] === '1' && !visited[row - 1][col]) {
                queue.push({
                    row: row - 1, col: col
                });
            }
            if (/*grid[col + 1] !== undefined &&*/ grid[row][col + 1] === '1' && !visited[row][col + 1]) {
                queue.push({
                    row: row, col: col + 1
                });
            }
            if (/*grid[col - 1] !== undefined &&*/ grid[row][col - 1] === '1' && !visited[row][col - 1]) {
                queue.push({
                    row: row, col: col - 1
                });
            }
        }
        islands++
    }
    return islands;

};

function isEmpty(array) {
    return array.length === 0;
}

